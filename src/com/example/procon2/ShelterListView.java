package com.example.procon2;

public class ShelterListView {
	public String shelterName;
	public String shelterAddress;
	public String shelterTel;
	public String shelterPerNum;
	public String shelterObject;

	public ShelterListView(String shelterName, String shelterAddress,
			String shelterTel, String shelterPerNum, String shelterObject) {
		this.shelterName = shelterName;
		this.shelterAddress = shelterAddress;
		this.shelterTel = shelterTel;
		this.shelterPerNum = shelterPerNum;
		this.shelterObject = shelterObject;
	}

}
